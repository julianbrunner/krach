namespace Multimedia.Graphics
{
	public static class Colors
	{
		public static Color Transparent { get { return Color.FromRgba(0, 0, 0, 0); } }
		public static Color Black { get { return Color.FromRgb(0, 0, 0); } }
		public static Color White { get { return Color.FromRgb(1, 1, 1); } }
		public static Color Red { get { return Color.FromRgb(1, 0, 0); } }
		public static Color Green { get { return Color.FromRgb(0, 1, 0); } }
		public static Color Blue { get { return Color.FromRgb(0, 0, 1); } }
		public static Color Yellow { get { return Color.FromRgb(1, 1, 0); } }
		public static Color Magenta { get { return Color.FromRgb(1, 0, 1); } }
		public static Color Cyan { get { return Color.FromRgb(0, 1, 1); } }
	}
}
